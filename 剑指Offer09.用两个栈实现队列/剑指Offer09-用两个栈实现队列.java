class CQueue {
    private Stack<Integer> stack1;
    private Stack<Integer> stack2;

    public CQueue() {
        stack1 = new Stack<Integer>();
        stack2 = new Stack<Integer>();
    }
    
    public void appendTail(int value) {
        stack1.push(value);
    }
    
    public int deleteHead() {
        if(stack2.isEmpty()){
            if(stack1.isEmpty()){
            return -1;
            }
            else{
                while(!(stack1.isEmpty())){
                    stack2.push(stack1.pop());
                }
            }
        }
        
        return stack2.pop();
    }
}

